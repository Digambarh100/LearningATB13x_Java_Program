package Test;

public class P1_AllPrimitiveDataTypes {
    public static void main(String[] args) {
        /*
        Write a Java program to declare variables of all primitive data types and print their default values.
        */

        byte a = 10;
        int b = 120;
        short c = 1452;
        long d = 576586758L;

        double e = 786790.987;
        float f = 5768.67F;

        char ch = 'A';
        String name = "Lipi";

        boolean is_lipi_married = false;

        System.out.println("byte: " +a);
        System.out.println("int: " +b);
        System.out.println("short: " +c);
        System.out.println("long: " +d);
        System.out.println("double: " +e);
        System.out.println("float: " +f);
        System.out.println("char: " +ch);
        System.out.println("String: " +name);
        System.out.println("boolean: " +is_lipi_married);

    }
}
